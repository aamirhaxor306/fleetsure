// 4. MONTHLY STATEMENT
// ═══════════════════════════════════════════════════════════════════════════

export function generateMonthlyStatement(data, tenantInfo) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: 'A4', margin: PAGE_MARGIN, bufferPages: true })
    const buffers = []
    doc.on('data', b => buffers.push(b))
    doc.on('end', () => resolve(Buffer.concat(buffers)))
    doc.on('error', reject)

    let y = drawHeader(doc, tenantInfo, 'MONTHLY STATEMENT')

    // Period
    doc.rect(PAGE_MARGIN, y, CONTENT_WIDTH, 28).fill(COLORS.light)
    doc.fontSize(10).fillColor(COLORS.text).font('Helvetica-Bold')
      .text(`Period: ${formatDate(data.startDate)} — ${formatDate(data.endDate)}`, PAGE_MARGIN + 10, y + 8, { width: CONTENT_WIDTH - 20 })
    y += 44

    // Summary box
    const summaryItems = [
      { label: 'Total Trips', value: String(data.totalTrips || 0) },
      { label: 'Total Revenue', value: formatCurrency(data.totalRevenue || 0) },
      { label: 'Total Expenses', value: formatCurrency(data.totalExpenses || 0) },
      { label: 'Net Profit', value: formatCurrency((data.totalRevenue || 0) - (data.totalExpenses || 0)) },
    ]

    const boxWidth = CONTENT_WIDTH / 4
    summaryItems.forEach((item, i) => {
      const bx = PAGE_MARGIN + i * boxWidth
      doc.rect(bx, y, boxWidth, 50).fill(i === 3 ? COLORS.primary : COLORS.light)
      doc.fontSize(8).fillColor(i === 3 ? '#94a3b8' : COLORS.muted).font('Helvetica')
        .text(item.label, bx + 8, y + 8, { width: boxWidth - 16 })
      doc.fontSize(14).fillColor(i === 3 ? COLORS.white : COLORS.text).font('Helvetica-Bold')
        .text(item.value, bx + 8, y + 24, { width: boxWidth - 16 })
    })
    y += 66

    // Trip list table
    if (data.trips && data.trips.length > 0) {
      doc.fontSize(11).fillColor(COLORS.text).font('Helvetica-Bold')
        .text('Trip Details', PAGE_MARGIN, y)
      y += 18

      const tripHeaders = [
        { label: '#', key: 'sno', width: 28, align: 'center' },
        { label: 'Date', key: 'date', width: 72 },
        { label: 'Vehicle', key: 'vehicle', width: 75 },
        { label: 'Route', key: 'route', width: 150 },
        { label: 'Distance', key: 'distance', width: 55, align: 'right' },
        { label: 'Revenue', key: 'revenue', width: 60, align: 'right' },
        { label: 'Profit', key: 'profit', width: 55, align: 'right' },
      ]

      const tripRows = data.trips.slice(0, 25).map((t, i) => ({
        sno: i + 1,
        date: formatDate(t.startDate || t.date),
        vehicle: t.vehicleNumber || t.vehicle || '-',
        route: `${t.origin || '-'} → ${t.destination || '-'}`,
        distance: t.distance ? `${t.distance} km` : '-',
        revenue: formatCurrency(t.freightAmount || t.revenue || 0),
        profit: formatCurrency((t.freightAmount || t.revenue || 0) - (t.totalExpenses || t.expenses || 0)),
      }))

      y = drawTable(doc, tripHeaders, tripRows, y)
    }

    // Vehicle-wise breakdown
    if (data.vehicleBreakdown && data.vehicleBreakdown.length > 0) {
      y += 10
      if (y > 650) { doc.addPage(); y = PAGE_MARGIN }

      doc.fontSize(11).fillColor(COLORS.text).font('Helvetica-Bold')
        .text('Vehicle-wise Breakdown', PAGE_MARGIN, y)
      y += 18

      const vHeaders = [
        { label: 'Vehicle', key: 'vehicle', width: 120 },
        { label: 'Trips', key: 'trips', width: 50, align: 'center' },
        { label: 'Distance', key: 'distance', width: 80, align: 'right' },
        { label: 'Revenue', key: 'revenue', width: 90, align: 'right' },
        { label: 'Expenses', key: 'expenses', width: 80, align: 'right' },
        { label: 'Profit', key: 'profit', width: 75, align: 'right' },
      ]

      y = drawTable(doc, vHeaders, data.vehicleBreakdown, y)
    }

    drawFooter(doc, tenantInfo)
    doc.end()
  })
}


// ── Number to words (Indian style, up to crores) ────────────────────────────

function numberToWords(n) {
  if (n === 0) return 'Zero'
  const ones = ['', 'One', 'Two', 'Three', 'Four', 'Five', 'Six', 'Seven', 'Eight', 'Nine',
    'Ten', 'Eleven', 'Twelve', 'Thirteen', 'Fourteen', 'Fifteen', 'Sixteen', 'Seventeen', 'Eighteen', 'Nineteen']
  const tens = ['', '', 'Twenty', 'Thirty', 'Forty', 'Fifty', 'Sixty', 'Seventy', 'Eighty', 'Ninety']

  const intPart = Math.floor(Math.abs(n))
  if (intPart === 0) return 'Zero'

  function twoDigit(num) {
    if (num < 20) return ones[num]
    return tens[Math.floor(num / 10)] + (num % 10 ? ' ' + ones[num % 10] : '')
  }

  function threeDigit(num) {
    if (num >= 100) return ones[Math.floor(num / 100)] + ' Hundred' + (num % 100 ? ' ' + twoDigit(num % 100) : '')
    return twoDigit(num)
  }

  let result = ''
  let rem = intPart

  if (rem >= 10000000) {
    result += threeDigit(Math.floor(rem / 10000000)) + ' Crore '
    rem %= 10000000
  }
  if (rem >= 100000) {
    result += twoDigit(Math.floor(rem / 100000)) + ' Lakh '
    rem %= 100000
  }
  if (rem >= 1000) {
    result += twoDigit(Math.floor(rem / 1000)) + ' Thousand '
    rem %= 1000
  }
  if (rem > 0) {
    result += threeDigit(rem)
  }

  return result.trim()
}
