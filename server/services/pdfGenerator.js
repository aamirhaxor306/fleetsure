// 1. PAYMENT RECEIPT
// ═══════════════════════════════════════════════════════════════════════════

export function generatePaymentReceipt(data, tenantInfo) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: 'A4', margin: PAGE_MARGIN })
    const buffers = []
    doc.on('data', b => buffers.push(b))
    doc.on('end', () => resolve(Buffer.concat(buffers)))
    doc.on('error', reject)

    let y = drawHeader(doc, tenantInfo, 'PAYMENT RECEIPT')

    // Receipt details box
    doc.rect(PAGE_MARGIN, y, CONTENT_WIDTH, 28).fill(COLORS.light)
    doc.fontSize(10).fillColor(COLORS.text).font('Helvetica-Bold')
      .text(`Receipt No: ${data.receiptNumber || 'PR-' + Date.now()}`, PAGE_MARGIN + 10, y + 8, { width: CONTENT_WIDTH / 2 })
    doc.fontSize(10).fillColor(COLORS.text).font('Helvetica')
      .text(`Date: ${formatDate(data.date)}`, PAGE_MARGIN + CONTENT_WIDTH / 2, y + 8, { width: CONTENT_WIDTH / 2 - 10, align: 'right' })
    y += 44

    // Fields
    y = drawFieldRow(doc, 'Paid To', data.paidTo, y)
    y = drawFieldRow(doc, 'Amount', formatCurrency(data.amount), y)
    y = drawFieldRow(doc, 'Purpose', data.purpose, y)
    y = drawFieldRow(doc, 'Payment Mode', data.paymentMode || 'Cash', y)
    if (data.referenceNumber) {
      y = drawFieldRow(doc, 'Reference / Txn No.', data.referenceNumber, y)
    }

    // Amount in words box
    y += 10
    doc.rect(PAGE_MARGIN, y, CONTENT_WIDTH, 30).fill(COLORS.light)
    doc.fontSize(9).fillColor(COLORS.muted).font('Helvetica')
      .text('Amount (in words):', PAGE_MARGIN + 10, y + 4)
    doc.fontSize(10).fillColor(COLORS.text).font('Helvetica-Bold')
      .text(numberToWords(Number(data.amount) || 0) + ' Rupees Only', PAGE_MARGIN + 10, y + 16, { width: CONTENT_WIDTH - 20 })
    y += 44

    // Notes
    if (data.notes) {
      doc.fontSize(9).fillColor(COLORS.muted).font('Helvetica').text('Notes:', PAGE_MARGIN, y)
      y += 14
      doc.fontSize(10).fillColor(COLORS.text).font('Helvetica').text(data.notes, PAGE_MARGIN, y, { width: CONTENT_WIDTH })
      y = doc.y + 10
    }

    // Signature area
    y = Math.max(y + 60, 650)
    doc.fontSize(9).fillColor(COLORS.muted).font('Helvetica')
      .text('Authorized Signatory', 595.28 - PAGE_MARGIN - 150, y, { width: 150, align: 'center' })
    drawHorizontalLine(doc, y - 4, COLORS.border)

    drawFooter(doc, tenantInfo)
    doc.end()
  })
}

// ═══════════════════════════════════════════════════════════════════════════
// 2. FREIGHT INVOICE
// ═══════════════════════════════════════════════════════════════════════════

export function generateFreightInvoice(data, tenantInfo) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: 'A4', margin: PAGE_MARGIN })
    const buffers = []
    doc.on('data', b => buffers.push(b))
    doc.on('end', () => resolve(Buffer.concat(buffers)))
    doc.on('error', reject)

    let y = drawHeader(doc, tenantInfo, 'FREIGHT INVOICE')

    // Invoice meta
    doc.rect(PAGE_MARGIN, y, CONTENT_WIDTH, 28).fill(COLORS.light)
    doc.fontSize(10).fillColor(COLORS.text).font('Helvetica-Bold')
      .text(`Invoice No: ${data.invoiceNumber || 'FI-' + Date.now()}`, PAGE_MARGIN + 10, y + 8, { width: CONTENT_WIDTH / 2 })
    doc.fontSize(10).fillColor(COLORS.text).font('Helvetica')
      .text(`Date: ${formatDate(data.date)}`, PAGE_MARGIN + CONTENT_WIDTH / 2, y + 8, { width: CONTENT_WIDTH / 2 - 10, align: 'right' })
    y += 44

    // Bill To
    doc.fontSize(10).fillColor(COLORS.muted).font('Helvetica').text('Bill To:', PAGE_MARGIN, y)
    y += 14
    doc.fontSize(11).fillColor(COLORS.text).font('Helvetica-Bold').text(data.billTo || '-', PAGE_MARGIN, y)
    y += 14
    if (data.billToAddress) {
      doc.fontSize(9).fillColor(COLORS.muted).font('Helvetica').text(data.billToAddress, PAGE_MARGIN, y, { width: CONTENT_WIDTH })
      y = doc.y + 6
    }
    y += 10

    // Trip details table
    const items = data.items || [{
      description: `Freight: ${data.origin || '-'} → ${data.destination || '-'}`,
      vehicle: data.vehicleNumber || '-',
      distance: data.distance ? `${data.distance} km` : '-',
      rate: data.freightRate ? formatCurrency(data.freightRate) : '-',
      amount: formatCurrency(data.freightAmount || data.amount || 0),
    }]

    const headers = [
      { label: '#', key: 'sno', width: 30, align: 'center' },
      { label: 'Description', key: 'description', width: 195 },
      { label: 'Vehicle', key: 'vehicle', width: 80 },
      { label: 'Distance', key: 'distance', width: 60, align: 'right' },
      { label: 'Rate', key: 'rate', width: 65, align: 'right' },
      { label: 'Amount', key: 'amount', width: 65, align: 'right' },
    ]

    const rows = items.map((it, i) => ({ sno: i + 1, ...it }))
    y = drawTable(doc, headers, rows, y)

    // Totals
    const subtotal = Number(data.freightAmount || data.amount || 0)
    const gstRate = Number(data.gstPercent || 0)
    const gstAmount = subtotal * gstRate / 100
    const total = subtotal + gstAmount

    y += 4
    const totalsX = 595.28 - PAGE_MARGIN - 200
    const drawTotalRow = (label, value, bold = false) => {
      const font = bold ? 'Helvetica-Bold' : 'Helvetica'
      const size = bold ? 12 : 10
      doc.fontSize(size).fillColor(COLORS.text).font(font)
        .text(label, totalsX, y, { width: 120, align: 'right' })
      doc.fontSize(size).fillColor(COLORS.text).font(font)
        .text(value, totalsX + 125, y, { width: 75, align: 'right' })
      y += bold ? 22 : 18
    }

    drawTotalRow('Subtotal', formatCurrency(subtotal))
    if (gstRate > 0) {
      drawTotalRow(`GST (${gstRate}%)`, formatCurrency(gstAmount))
    }
    drawHorizontalLine(doc, y - 4, COLORS.border)
    drawTotalRow('Total', formatCurrency(total), true)

    // Notes
    if (data.notes) {
      y += 10
      doc.fontSize(9).fillColor(COLORS.muted).font('Helvetica').text('Notes:', PAGE_MARGIN, y)
      y += 14
      doc.fontSize(9).fillColor(COLORS.text).font('Helvetica').text(data.notes, PAGE_MARGIN, y, { width: CONTENT_WIDTH })
    }

    drawFooter(doc, tenantInfo)
    doc.end()
  })
}

