/**
 * Fleetsure — PDF Document Generator
 * ────────────────────────────────────
 * Generates PDFs for: Payment Receipt, Freight Invoice, Letterhead, Monthly Statement.
 * Uses PDFKit (pure Node.js, no headless browser).
 */
import PDFDocument from 'pdfkit'

// ── Shared constants ────────────────────────────────────────────────────────

const COLORS = {
  primary: '#1e3a5f',
  accent: '#2563eb',
  text: '#1e293b',
  muted: '#64748b',
  light: '#f1f5f9',
  border: '#cbd5e1',
  white: '#ffffff',
}

const PAGE_MARGIN = 50
const CONTENT_WIDTH = 595.28 - PAGE_MARGIN * 2 // A4 width minus margins

// ── Helpers ─────────────────────────────────────────────────────────────────

function formatCurrency(amount) {
  const num = Number(amount) || 0
  return '₹' + num.toLocaleString('en-IN', { minimumFractionDigits: 2, maximumFractionDigits: 2 })
}

function formatDate(dateStr) {
  if (!dateStr) return new Date().toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' })
  return new Date(dateStr).toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' })
}

function drawHorizontalLine(doc, y, color = COLORS.border) {
  doc.strokeColor(color).lineWidth(0.5)
    .moveTo(PAGE_MARGIN, y)
    .lineTo(595.28 - PAGE_MARGIN, y)
    .stroke()
}

// ── Common Header (company branding) ────────────────────────────────────────

function drawHeader(doc, tenantInfo, docTitle) {
  const companyName = tenantInfo?.name || 'Fleet Company'
  const address = tenantInfo?.address || ''
  const city = tenantInfo?.city || ''
  const phone = tenantInfo?.phone || ''
  const gstin = tenantInfo?.gstin || ''

  // Company name
  doc.fontSize(20).fillColor(COLORS.primary).font('Helvetica-Bold')
    .text(companyName, PAGE_MARGIN, PAGE_MARGIN, { width: CONTENT_WIDTH })

  // Address line
  let infoY = doc.y + 4
  const infoParts = [address, city].filter(Boolean).join(', ')
  if (infoParts) {
    doc.fontSize(9).fillColor(COLORS.muted).font('Helvetica')
      .text(infoParts, PAGE_MARGIN, infoY, { width: CONTENT_WIDTH })
    infoY = doc.y + 2
  }

  // Phone & GSTIN line
  const detailParts = []
  if (phone) detailParts.push(`Phone: ${phone}`)
  if (gstin) detailParts.push(`GSTIN: ${gstin}`)
  if (detailParts.length) {
    doc.fontSize(9).fillColor(COLORS.muted).font('Helvetica')
      .text(detailParts.join('  |  '), PAGE_MARGIN, infoY, { width: CONTENT_WIDTH })
    infoY = doc.y + 2
  }

  // Horizontal rule
  const lineY = infoY + 8
  drawHorizontalLine(doc, lineY, COLORS.primary)

  // Document title
  doc.fontSize(14).fillColor(COLORS.accent).font('Helvetica-Bold')
    .text(docTitle, PAGE_MARGIN, lineY + 12, { width: CONTENT_WIDTH, align: 'center' })

  return doc.y + 16
}

// ── Common Footer ───────────────────────────────────────────────────────────

function drawFooter(doc, tenantInfo) {
  const bottomY = 780
  drawHorizontalLine(doc, bottomY, COLORS.border)
  doc.fontSize(7).fillColor(COLORS.muted).font('Helvetica')
    .text('This is a computer-generated document.', PAGE_MARGIN, bottomY + 6, { width: CONTENT_WIDTH, align: 'center' })
  const companyName = tenantInfo?.name || 'Fleet Company'
  doc.text(`${companyName} — Generated by Fleetsure`, PAGE_MARGIN, bottomY + 16, { width: CONTENT_WIDTH, align: 'center' })
}

// ── Field row helper ────────────────────────────────────────────────────────

function drawFieldRow(doc, label, value, y, options = {}) {
  const labelWidth = options.labelWidth || 140
  doc.fontSize(10).fillColor(COLORS.muted).font('Helvetica')
    .text(label, PAGE_MARGIN, y, { width: labelWidth })
  doc.fontSize(10).fillColor(COLORS.text).font('Helvetica-Bold')
    .text(String(value || '-'), PAGE_MARGIN + labelWidth, y, { width: CONTENT_WIDTH - labelWidth })
  return y + 20
}

// ── Table helper ────────────────────────────────────────────────────────────

function drawTable(doc, headers, rows, startY) {
  const colWidths = headers.map(h => h.width)
  let y = startY

  // Header row
  doc.rect(PAGE_MARGIN, y, CONTENT_WIDTH, 22).fill(COLORS.primary)
  let x = PAGE_MARGIN
  headers.forEach((h, i) => {
    doc.fontSize(9).fillColor(COLORS.white).font('Helvetica-Bold')
      .text(h.label, x + 6, y + 6, { width: colWidths[i] - 12, align: h.align || 'left' })
    x += colWidths[i]
  })
  y += 22

  // Data rows
  rows.forEach((row, rowIdx) => {
    const bgColor = rowIdx % 2 === 0 ? COLORS.white : COLORS.light
    doc.rect(PAGE_MARGIN, y, CONTENT_WIDTH, 20).fill(bgColor)

    x = PAGE_MARGIN
    headers.forEach((h, i) => {
      doc.fontSize(9).fillColor(COLORS.text).font('Helvetica')
        .text(String(row[h.key] ?? '-'), x + 6, y + 5, { width: colWidths[i] - 12, align: h.align || 'left' })
      x += colWidths[i]
    })
    y += 20
  })

  // Bottom border
  drawHorizontalLine(doc, y, COLORS.border)
  return y + 8
}

