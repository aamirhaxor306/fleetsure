// 3. LETTERHEAD
// ═══════════════════════════════════════════════════════════════════════════

export function generateLetterhead(data, tenantInfo) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: 'A4', margin: PAGE_MARGIN })
    const buffers = []
    doc.on('data', b => buffers.push(b))
    doc.on('end', () => resolve(Buffer.concat(buffers)))
    doc.on('error', reject)

    let y = drawHeader(doc, tenantInfo, '')

    // Date and reference
    doc.fontSize(10).fillColor(COLORS.text).font('Helvetica')
      .text(`Date: ${formatDate(data.date)}`, PAGE_MARGIN, y, { width: CONTENT_WIDTH })
    y = doc.y + 4
    if (data.referenceNumber) {
      doc.fontSize(10).fillColor(COLORS.text).font('Helvetica')
        .text(`Ref: ${data.referenceNumber}`, PAGE_MARGIN, y, { width: CONTENT_WIDTH })
      y = doc.y + 4
    }
    y += 12

    // To
    if (data.to) {
      doc.fontSize(10).fillColor(COLORS.muted).font('Helvetica').text('To,', PAGE_MARGIN, y)
      y += 14
      doc.fontSize(10).fillColor(COLORS.text).font('Helvetica').text(data.to, PAGE_MARGIN, y, { width: CONTENT_WIDTH })
      y = doc.y + 16
    }

    // Subject
    if (data.subject) {
      doc.fontSize(11).fillColor(COLORS.text).font('Helvetica-Bold')
        .text(`Subject: ${data.subject}`, PAGE_MARGIN, y, { width: CONTENT_WIDTH })
      y = doc.y + 16
    }

    // Body text
    if (data.body) {
      doc.fontSize(10).fillColor(COLORS.text).font('Helvetica')
        .text(data.body, PAGE_MARGIN, y, { width: CONTENT_WIDTH, lineGap: 4 })
    }

    drawFooter(doc, tenantInfo)
    doc.end()
  })
}

